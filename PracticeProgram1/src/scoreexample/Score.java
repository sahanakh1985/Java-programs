package scoreexample;

public class Score {
	int subject1;
	int subject2;
	int subject3;
	
	public Score(int subject1, int subject2, int subject3) {
		this.subject1 = subject1;
		this.subject2 = subject2;
		this.subject3 = subject3;
	}

	@Override
	public String toString() {
		return "Score [subject1=" + subject1 + ", subject2=" + subject2 + ", subject3=" + subject3 + "]";
	}
	
	
	
	
	
	

}
