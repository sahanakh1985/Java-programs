
public class TernaryOperatorExample1 {
	public static void main(String[] args) {
		// ? and : are ternary operators,used to optimize the code
		// here, '?' will prints true statement and ':' will prints false statement
		int a = 10;
		int b = 20;
		System.out.println((a >= b) ? "smaller" : "bigger");
	//	here a>=b(10>=20) , is a false condition , so it prints "bigger" 
	}

}
