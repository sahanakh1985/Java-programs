
public class ElseIfConditionalOperators {
	public static void main(String[] args) {
		int marks = 71;
		int avg = 75;
		if (marks >= 50 && avg <= 65) {
			System.out.println("ABC");
		} else if (marks >= 70 && avg >= 72) {
			System.out.println("XYZ");
		} else {
			System.out.println("MNO");
		}
	}

}
