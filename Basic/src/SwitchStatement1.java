
public class SwitchStatement1 {
	public static void main(String[] args) {
		int x = 2;
		switch (x) {
		case 3:
			System.out.println("Three");
			break;
		case 2:
			System.out.println("Two");
			break;
		case 1:
			System.out.println("One");
			break;
		default:
			System.out.println("Some other number");

		}
	}

}
