package com.day4.operators;

import java.util.Scanner;

public class Sampeloperator2 {
	public static void main(String[] args) {
		int m=10;
		int n=20;
		System.out.println(m < n);
		System.out.println(m > n);
		System.out.println(m == n);
		System.out.println(m != n);
		System.out.println(m >= n);
		System.out.println(m <= n);
	}

}
